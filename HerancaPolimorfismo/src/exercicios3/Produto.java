package exercicios3;

public class Produto {

	private String nome;
	private String marca;
	
	public Produto(String nome, String marca) {
		super();
		this.nome = nome;
		this.marca = marca;

	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getMarca() {
		return marca;
	}

	public void setMarca(String marca) {
		this.marca = marca;
	}


	
	
	
}
